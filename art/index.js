var piece = function(draw, row, col, type, color, boarderColor)
{
    type = type % 19;
    var fun;
    if (type < 4)
    {
        fun = L;
    }
    else if (type < 8)
    {
        fun = J;
    }
    else if (type < 12)
    {
        fun = T;
    }
    else if (type < 14)
    {
        fun = Z;
    }
    else if (type < 16)
    {
        fun = S;
    }
    else if (type < 18)
    {
        fun = I;
    }
    else
    {
        fun = O;
    }
    
    fun(draw, row, col, type, color, boarderColor);
}

